import { EmbedBuilder } from "discord.js";

const createEmbed = (uuid, first_name, last_name, birthday, currentPage = 0, totalPages = 1) => {
  return (
    new EmbedBuilder()
      // .setColor(0xd11013)
      .setColor(0x6943e8)
      .setTitle(first_name + " " + last_name + "'s Info")
      // .setAuthor({
      //   name: "GovLink",
      //   iconURL: "http://localhost:3000/assets/govlink-logo.png", // Full URL needed
      //   url: "https://youtube.com",
      // })
      .addFields(
        { name: "UUID", value: uuid },
        { name: "\u200B", value: "\u200B" },
        { name: "First Name", value: first_name, inline: true },
        { name: "Last Name", value: last_name, inline: true },
        { name: "Birthday", value: birthday, inline: true }
      )
      .setFooter({ text: `Page ${currentPage + 1}/${totalPages} â€¢ Generated by FakerAPI` })
      .setTimestamp()
  );
};

